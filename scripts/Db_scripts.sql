-- Finder Testers database script.

CREATE TABLE TESTERS (
   tester_id INT NOT NULL AUTO_INCREMENT,
   first_name VARCHAR(45) NOT NULL,
   last_name VARCHAR(45) NOT NULL,
   country VARCHAR(45) NOT NULL,
   last_login DATETIME,
   PRIMARY KEY ( tester_id ),
   CONSTRAINT IDX1 (country)
);

CREATE TABLE DEVICES(
  device_id     INT NOT NULL AUTO_INCREMENT,
  description VARCHAR(45) NOT NULL,
  CONSTRAINT device_pk PRIMARY KEY (device_id)
);

CREATE TABLE TESTER_DEVICE(
   tester_id INT NOT NULL AUTO_INCREMENT, 
   device_id INT NOT NULL,
   CONSTRAINT tester_device_pk PRIMARY KEY (tester_id, device_id)
);

ALTER TABLE TESTER_DEVICE ADD CONSTRAINT tester_fk FOREIGN KEY (tester_id) REFERENCES TESTERS(tester_id);
ALTER TABLE TESTER_DEVICE ADD CONSTRAINT device_fk FOREIGN KEY (device_id) REFERENCES DEVICES(device_id);

CREATE TABLE BUGS (
  bug_id    INT  NOT NULL AUTO_INCREMENT,
  device_id INT  NOT NULL,
  tester_id INT  NOT NULL,
  CONSTRAINT bug_pk PRIMARY KEY (bug_id)
);

ALTER TABLE BUGS ADD CONSTRAINT bugs_tester_fk FOREIGN KEY (tester_id) REFERENCES TESTERS(tester_id);
ALTER TABLE BUGS ADD CONSTRAINT bugs_device_fk FOREIGN KEY (device_id) REFERENCES DEVICES(device_id);


